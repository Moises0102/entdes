package ejercicios;

public class metodos {

	public static void main(String[] args) {
		hola();
		numero();
	}

	
	public static void hola() {
		
		String a = "ho";
		String b = "la";
		
		String c = a + b;
		
		System.out.println(c);
	}
	
	
	
	public static void numero() {
		
		char a = 67;
		
		System.out.println(a);
		
	}
	
	public static void asci() {
		
		char b = 8;
		
		double c = (double) b;
		
		System.out.println(c);
	} 
}
